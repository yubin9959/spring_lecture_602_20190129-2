package com.java.classic;

public class FloatCalculator2 extends FloatCalculator {
	
	public float sum(float a,float b) {
		return (a+b)*10;
	}
	public float sub(float a,float b) {
		return (a-b)*10;
	}
	public double multi(float a, float b) {
		return (double)a*b*10;
	}
	public double div(float a, float b) {
		return (double)a/b*10;
		
	} 
}
